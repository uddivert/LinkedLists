public class NodeType {
    public ItemType info;
    public NodeType next;

    /**
     * Constructor for NodeType
     */
    public NodeType(ItemType item) {
        item = info;
    }

    /**
     * This will set the next Node
     */
    public next(NodeType nextOne) {
        nextOne = next;
    }// next

    /**
     * returns next node
     */
    public getNext() {
        return next;
    } // getNext
}

